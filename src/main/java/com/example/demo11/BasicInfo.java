package com.example.demo11;

public class BasicInfo {
	private String id;
	private String name;
	
	public BasicInfo() {
		super();
	}
	
	public BasicInfo(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	public String getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	

}
